from math import sin, cos, tan, radians
angulo = float(input('Digite um 창ngulo qualquer: '))
print('O valor do seno do 창ngulo: {}'.format(sin(radians(angulo))))
print('O valor do cosseno do 창ngulo: {}'.format(cos(radians(angulo))))
print('O valor da tangente desse 창ngulo: {}'.format(tan(radians(angulo))))